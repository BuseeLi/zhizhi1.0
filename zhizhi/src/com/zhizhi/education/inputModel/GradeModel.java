package com.zhizhi.education.inputModel;
import com.zhizhi.common.base.BaseModel;
/**
 * 
 * @author taotao  2017.11.16
 *
 */
@SuppressWarnings("serial")
public class GradeModel extends BaseModel<GradeModel>{
	
	private String id;
	
	private String courseId;
	
	private String name;
	
	private Integer capatity;
	// 班级容量
	private String startDate;
	
	private String classroom;
	
	private String teacherId;
	
	private Integer courseTime;
	
	private String remark;
	
	public GradeModel() {}
	
	public String getTeacherId() {
		return teacherId;
	}

	public void setTeacherId(String teacherId) {
		this.teacherId = teacherId;
	}

	public String getRemark() {
		return remark;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Integer getCapatity() {
		return capatity;
	}

	public void setCapatity(Integer capatity) {
		this.capatity = capatity;
	}

	public String getClassroom() {
		return classroom;
	}

	public void setClassroom(String classroom) {
		this.classroom = classroom;
	}

	public Integer getCourseTime() {
		return courseTime;
	}

	public void setCourseTime(Integer courseTime) {
		this.courseTime = courseTime;
	}

	public String getStartDate() {
		return startDate;
	}

	public void setStartDate(String startDate) {
		this.startDate = startDate;
	}

	public String getCourseId() {
		return courseId;
	}

	public void setCourseId(String courseId) {
		this.courseId = courseId;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	};
	
}
