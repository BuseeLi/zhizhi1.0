package com.zhizhi.education.inputModel;

import java.math.BigDecimal;

public class StudentModel {
	
	private String id;
	
	private String name;
	
	private String parentName;
	
	private String tel;
	
	private String idNumber;
	
	private String joinCourse;
	
	private String payType;
	
	private BigDecimal payCount;
	
	private String keepPerson;
	
	public StudentModel() {}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getTel() {
		return tel;
	}

	public void setTel(String tel) {
		this.tel = tel;
	}

	public String getIdNumber() {
		return idNumber;
	}

	public void setIdNumber(String idNumber) {
		this.idNumber = idNumber;
	}

	public String getJoinCourse() {
		return joinCourse;
	}

	public void setJoinCourse(String joinCourse) {
		this.joinCourse = joinCourse;
	}

	public String getPayType() {
		return payType;
	}

	public void setPayType(String payType) {
		this.payType = payType;
	}

	public BigDecimal getPayCount() {
		return payCount;
	}

	public void setPayCount(BigDecimal payCount) {
		this.payCount = payCount;
	}

	public String getKeepPerson() {
		return keepPerson;
	}

	public void setKeepPerson(String keepPerson) {
		this.keepPerson = keepPerson;
	}

	public String getParentName() {
		return parentName;
	}

	public void setParentName(String parentName) {
		this.parentName = parentName;
	}
	
	
}
