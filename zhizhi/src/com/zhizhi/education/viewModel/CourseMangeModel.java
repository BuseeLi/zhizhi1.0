package com.zhizhi.education.viewModel;

import java.math.BigDecimal;
import java.util.Date;

import com.zhizhi.common.base.BaseModel;

public class CourseMangeModel extends BaseModel<CourseMangeModel>{

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	private String id;
	private String courseName;
	private BigDecimal price;
	private Integer couseTime;
	private Integer gradeNum;
	private Integer studentNum;
	private Date createTime;
	private String others;
	
	public CourseMangeModel(){}
	
	
	
	public CourseMangeModel(String id, String courseName, BigDecimal price, Integer couseTime, Integer gradeNum,
			Integer studentNum, Date createTime, String others) {
		super();
		this.id = id;
		this.courseName = courseName;
		this.price = price;
		this.couseTime = couseTime;
		this.gradeNum = gradeNum;
		this.studentNum = studentNum;
		this.createTime = createTime;
		this.others = others;
	}



	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}



	public String getCourseName() {
		return courseName;
	}

	public void setCourseName(String courseName) {
		this.courseName = courseName;
	}



	public BigDecimal getPrice() {
		return price;
	}

	public void setPrice(BigDecimal price) {
		this.price = price;
	}

	public Integer getCouseTime() {
		return couseTime;
	}

	public void setCouseTime(Integer couseTime) {
		this.couseTime = couseTime;
	}

	public Integer getGradeNum() {
		return gradeNum;
	}

	public void setGradeNum(Integer gradeNum) {
		this.gradeNum = gradeNum;
	}

	public Integer getStudentNum() {
		return studentNum;
	}

	public void setStudentNum(Integer studentNum) {
		this.studentNum = studentNum;
	}

	public Date getCreateTime() {
		return createTime;
	}

	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}

	public String getOthers() {
		return others;
	}

	public void setOthers(String others) {
		this.others = others;
	}

	public static long getSerialversionuid() {
		return serialVersionUID;
	}



	@Override
	public String toString() {
		return "CourseMangeModel [id=" + id + ", courseName=" + courseName + ", price=" + price + ", couseTime="
				+ couseTime + ", gradeNum=" + gradeNum + ", studentNum=" + studentNum + ", createTime=" + createTime
				+ ", others=" + others + "]";
	}


	
	
	
}
